package com.xwq.qingyouapp.bean;

public class GradeBT {

	private Short gradeleft, graderight;

	public GradeBT(Short gradeleft, Short graderight) {
		super();
		this.gradeleft = gradeleft;
		this.graderight = graderight;
	}

	public Short getGradeleft() {
		return gradeleft;
	}

	public void setGradeleft(Short gradeleft) {
		this.gradeleft = gradeleft;
	}

	public Short getGraderight() {
		return graderight;
	}

	public void setGraderight(Short graderight) {
		this.graderight = graderight;
	}

}
