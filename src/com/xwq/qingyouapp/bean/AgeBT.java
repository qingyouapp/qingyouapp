package com.xwq.qingyouapp.bean;

public class AgeBT {

	private Integer ageleft, ageright;

	public AgeBT(Integer ageleft, Integer ageright) {
		super();
		this.ageleft = ageleft;
		this.ageright = ageright;
	}

	public Integer getAgeleft() {
		return ageleft;
	}

	public void setAgeleft(Integer ageleft) {
		this.ageleft = ageleft;
	}

	public Integer getAgeright() {
		return ageright;
	}

	public void setAgeright(Integer ageright) {
		this.ageright = ageright;
	}

}
