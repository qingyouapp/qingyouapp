package com.xwq.qingyouapp.bean;

public class WeightBT {

	private Short weightleft, weightright;

	public WeightBT(Short weightleft, Short weightright) {
		super();
		this.weightleft = weightleft;
		this.weightright = weightright;
	}

	public Short getWeightleft() {
		return weightleft;
	}

	public void setWeightleft(Short weightleft) {
		this.weightleft = weightleft;
	}

	public Short getWeightright() {
		return weightright;
	}

	public void setWeightright(Short weightright) {
		this.weightright = weightright;
	}
}
