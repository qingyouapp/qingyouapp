package com.xwq.qingyouapp.bean;

public class HeightBT {

	private Short heightleft, heightright;

	public HeightBT(Short heightleft, Short heightright) {
		super();
		this.heightleft = heightleft;
		this.heightright = heightright;
	}

	public Short getHeightleft() {
		return heightleft;
	}

	public void setHeightleft(Short heightleft) {
		this.heightleft = heightleft;
	}

	public Short getHeightright() {
		return heightright;
	}

	public void setHeightright(Short heightright) {
		this.heightright = heightright;
	}

}
